#include "libft.h"

int	ft_atoi(const char *c)
{
	int	x;
	int	n;

	while ((*c >= '\t' && *c <= '\r') || *c == ' ')
		++c;
	x = 1;
	if (*c == '+' || *c == '-')
	{
		if (*c++ == '-')
			x = -1;
	}
	n = 0;
	while (ft_isdigit(*c))
	{
		n = n * 10 + (*c - '0');
		++c;
	}
	return (n * x);
}
